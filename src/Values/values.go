package main

import "fmt"

func main() {
	fmt.Println("Go" + "Playground")
	fmt.Println("1 + 1 = ", 1+1)
	fmt.Println("8.0 / 3.0 = ", 8.0/3.0) // = 2.66
	fmt.Println("8.0 / 3 = ", 8.0/3)     // = 2.66..
	fmt.Println("8 / 3 = ", 8/3)         // = 2
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
